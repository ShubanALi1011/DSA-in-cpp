#include<iostream>
using namespace std;
int main(){
    // int 
    int a=5;
    cout<<"Value of int a : "<<a<<endl;
    cout<<"Size of int a : "<<sizeof(a)<<endl;

    // charactor
    char ch='a';
    cout<<"Value of char ch : "<<ch<<endl;
    cout<<"Size of char ch : "<<sizeof(ch)<<endl;

    // float
    float f=1.2;
    cout<<"Value of float f : "<<f<<endl;
    cout<<"Size of float f : "<<sizeof(f)<<endl;

    // bool
    bool b = true;
    cout<<"Value of bool b : "<<b<<endl;
    cout<<"Size of bool b : "<<sizeof(b)<<endl;

    // double
    double d = 4.2;
    cout<<"Value of double d : "<<d<<endl;
    cout<<"Size of double d : "<<sizeof(d)<<endl;

    
    return 0;
}